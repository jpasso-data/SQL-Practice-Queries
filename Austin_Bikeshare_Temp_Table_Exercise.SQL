-- Austin Bikeshare Temp Table Analysis
-- Dataset: bigquery-public-data.austin_bikeshare
-- Tables: bikeshare_trips
-- Author: Joe Passo | jpasso-data
-- Course: Google Data Analytics Certificate - Course 5
-- Date: May 2026
-- Note: Uses a CTE (WITH clause) to identify the bike with the
--       longest total trip duration, then joins back to the
--       original table to find its most frequent starting station.
-- ========================================

WITH
  longest_used_bike AS (
    SELECT
      bike_id,
      SUM(duration_minutes) AS trip_duration
    FROM
      bigquery-public-data.austin_bikeshare.bikeshare_trips
    GROUP BY
      bike_id
    ORDER BY
      trip_duration DESC
    LIMIT 1
  )

## find the station where the longest-used bike leaves most often
SELECT
  trips.start_station_id,
  COUNT(*) AS trip_ct,
FROM
  longest_used_bike AS longest
INNER JOIN
  bigquery-public-data.austin_bikeshare.bikeshare_trips AS trips
ON longest.bike_id = trips.bike_id
GROUP BY
  trips.start_station_id
ORDER BY
  trip_ct DESC
LIMIT 1
